from math import pow

print("%d\n"%(3+4))
print("%d\n"%(3-4))
print("%d\n"%(3*4))
print("%d\n"%(3//4)) #몫
print("%lf\n"%(3.0/4))#끝까지 나누기
print("%d\n"%(3%4))#나머지
print("%lf\n"%pow(3,4))# 거듭제곱

print("%d\n"%(3>4))#0
print("%d\n"%(3>=4))#0
print("%d\n"%(3<4))#1
print("%d\n"%(3<=4))#1
print("%r\n"%(3==4))#0
print("%s\n"%(3!=4))#1

